    public static void batchRequest(List<String> ids) {
        System.out.println("开始批量请求，共 " + ids.size() + " 个请求");
        
        for (int i = 0; i < ids.size(); i++) {
            String id = ids.get(i);
            System.out.println("正在处理第 " + (i + 1) + " 个请求，ID: " + id);
            
            try {
                String result = sendGetRequest(BASE_URL + id);
                System.out.println("ID " + id + " 的响应: " + result);
                
                // 在请求之间添加延迟，防止请求过于频繁
                // 根据经验教训，添加500ms延迟
                if (i < ids.size() - 1) { // 最后一个请求不需要延迟
                    Thread.sleep(500);
                }
            } catch (Exception e) {
                System.err.println("处理ID " + id + " 时发生错误: " + e.getMessage());
                e.printStackTrace();
            }
        }
        
        System.out.println("批量请求完成");
    }
    
    public static String sendGetRequest(String url) throws IOException {
        System.out.println("发送请求到: " + url);
        
        // 构建请求
        Request request = new Request.Builder()
                .url(url)
                .get()
                .build();
        
        // 执行请求
        Response response = client.newCall(request).execute();
        
        // 输出响应状态码
        System.out.println("状态码: " + response.code());
        
        // 获取响应体
        String responseBody = "";
        if (response.body() != null) {
            responseBody = response.body().string();
        }
        
        // 关闭响应
        response.close();
        
        return responseBody;
    }
